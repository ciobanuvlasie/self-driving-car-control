#define OLED_I2C   LPI2C2
#define OLED_ADDR  0x3C

#define OLED_W 128
#define OLED_H 64
#define OLED_PAGES (OLED_H/8)

static uint8_t g_fb[OLED_W * OLED_PAGES];

// ===== I2C write =====
static status_t oled_i2c_write(uint8_t addr7, const uint8_t *data, size_t len)
{
    lpi2c_master_transfer_t t = {0};
    t.slaveAddress = addr7;
    t.direction    = kLPI2C_Write;
    t.data         = (uint8_t*)data;
    t.dataSize     = len;
    return LPI2C_MasterTransferBlocking(OLED_I2C, &t);
}

static void oled_cmd(uint8_t c)
{
    uint8_t buf[2] = {0x00, c};
    oled_i2c_write(OLED_ADDR, buf, 2);
}

static void oled_cmd_list(const uint8_t *cmds, size_t n)
{
    uint8_t buf[32];
    buf[0] = 0x00;
    memcpy(&buf[1], cmds, n);
    oled_i2c_write(OLED_ADDR, buf, n+1);
}

