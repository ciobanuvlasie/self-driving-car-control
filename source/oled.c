static const uint8_t glyph_space[5]={0};
static const uint8_t glyph_dot[5]={0x00,0x60,0x60,0x00,0x00};
// litera mica 'm' (5x7 aproximativ)
static const uint8_t glyph_m[5] = {
    0x7C,  // █████
    0x04,  //     █
    0x18,  //   ██
    0x04,  //     █
    0x78   // ████
};
// caracter '=' (5x7)
static const uint8_t glyph_equal[5] = {
    0x14,  //  █ █
    0x14,  //  █ █
    0x14,  //  █ █
    0x14,  //  █ █
    0x14   //  █ █
};



static const uint8_t* font_get(char c)
{
    if(c>='0'&&c<='9') return font_digits[c-'0'];
    if(c>='A'&&c<='Z') return font_caps[c-'A'];
    if(c=='.') return glyph_dot;
    if (c == 'm') return glyph_m;
    if (c == '=') return glyph_equal;
    return glyph_space;
}

// ===== framebuffer =====

static void setpx(int x,int y,int on)
{
    if(x<0||x>=OLED_W||y<0||y>=OLED_H) return;
    uint32_t i=x+(y/8)*OLED_W;
    if(on) g_fb[i]|=1<<(y&7);
    else g_fb[i]&=~(1<<(y&7));
}

void oled_clear(void){ memset(g_fb,0,sizeof(g_fb)); }

void oled_print(int x,int y,const char*s)
{
    while(*s){
        const uint8_t*g=font_get(*s++);
        for(int c=0;c<5;c++)
            for(int r=0;r<7;r++)
                setpx(x+c,y+r,(g[c]>>r)&1);
        x+=6;
    }
}

void oled_update(void)
{
    uint8_t cmds[]={0x20,0x00,0x21,0,127,0x22,0,7};
    oled_cmd_list(cmds,sizeof(cmds));

    uint8_t pkt[17]; pkt[0]=0x40;
    for(int i=0;i<1024;i+=16){
        memcpy(&pkt[1],&g_fb[i],16);
        oled_i2c_write(OLED_ADDR,pkt,17);
    }
}

void oled_init(void)
{
    uint8_t init[]={0xAE,0xD5,0x80,0xA8,0x3F,0xD3,0,0x40,0x8D,0x14,
                    0x20,0,0xA1,0xC8,0xDA,0x12,0x81,0xCF,0xD9,0xF1,
                    0xDB,0x40,0xA4,0xA6,0xAF};
    oled_cmd_list(init,sizeof(init));
    oled_clear();
    oled_update();
}
